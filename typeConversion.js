// Type conversion = change the datetype of a value to another
//  Strings, Numbers, Booleans
// typeof lets us know the datatype

// User input accepts only string
// Use number to convert string to number

let age = window.prompt("How old are you?");

age = Number(age);

// ` ` Ternaray operator lets us use ${variableHere}
console.log(`you are ${age} years old!`);

